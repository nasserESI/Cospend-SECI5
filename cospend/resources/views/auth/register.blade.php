<script>
document.getElementById('register-form').addEventListener('submit', function(event) {
    event.preventDefault();

    window.crypto.subtle.generateKey(
        {
            name: "RSA-OAEP",
            modulusLength: 2048,
            publicExponent: new Uint8Array([0x01, 0x00, 0x01]),
            hash: "SHA-256",
        },
        true,
        ["encrypt", "decrypt"]
    )
    .then(function(key){
        window.crypto.subtle.exportKey(
            "jwk",
            key.publicKey
        )
        .then(function(keydata){
            // Store the public key in the hidden input field
            document.getElementById('public-key').value = JSON.stringify(keydata);
            console.log('public_key :'+ keydata);

            // Store the private key in local storage
            let email = document.getElementById('email').value;
            window.crypto.subtle.exportKey(
                "jwk",
                key.privateKey
            )
            .then(function(keydata){
                localStorage.setItem(email + '_privateKey', JSON.stringify(keydata));
                
                // Submit the form manually
                document.getElementById('register-form').submit();
            })
            .catch(function(err){
                console.error(err);
            });
        })
        .catch(function(err){
            console.error(err);
        });
    })
    .catch(function(err){
        console.error(err);
    });
});
</script>
